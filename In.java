public class In
{
    static Scanner In = new Scanner(System.in);
    
    public static int nextInt()
    {
        return In.nextInt();
    }

    public static char nextChar()
    {
        String i = In.nextLine();
        return i.charAt(0);
    }

    public static String nextLine()
    {
        return In.nextLine();
    }
}
